// scripts/gen-world-paths.js
// Regenerates public/scripts/world-paths.js — the inline-SVG country shapes
// used by the language map overlay (public/scripts/lang-map.js).
//
// The generator deps are NOT in package.json (one-off tooling). To run:
//   npm i --no-save topojson-client d3-geo
//   curl -sL -o /tmp/countries-50m.json https://cdn.jsdelivr.net/npm/world-atlas@2/countries-50m.json
//   node scripts/gen-world-paths.js /tmp/countries-50m.json
//
// Output: window.APERIO_WORLD_PATHS = [{a2, name, d}] — a2 is ISO 3166-1
// alpha-2 (null for countries we don't need to address), d is the SVG path in
// a 4000×2073 Natural Earth projection, integer coordinates.
import { readFileSync, writeFileSync } from "fs";
import * as topojson from "topojson-client";
import { geoNaturalEarth1, geoPath } from "d3-geo";

const src = process.argv[2];
if (!src) { console.error("usage: node scripts/gen-world-paths.js <countries-50m.json>"); process.exit(1); }

const topo = JSON.parse(readFileSync(src, "utf8"));
const geo  = topojson.feature(topo, topo.objects.countries);

const W = 4000;
const projection = geoNaturalEarth1().fitWidth(W, geo);
const path = geoPath(projection);
// Integer coordinates keep the payload small; at W=4000 the error is sub-pixel.
const fmt = (d) => d.replace(/(\d+)\.\d+/g, "$1");

// ISO 3166-1 numeric → alpha-2. Only countries a language may need to address;
// extend this map when adding a language whose countries are missing.
const N2A = {
  "040":"AT","056":"BE","100":"BG","191":"HR","196":"CY","203":"CZ","208":"DK",
  "233":"EE","246":"FI","250":"FR","276":"DE","300":"GR","348":"HU","372":"IE",
  "380":"IT","428":"LV","440":"LT","442":"LU","470":"MT","528":"NL","616":"PL",
  "620":"PT","642":"RO","703":"SK","705":"SI","724":"ES","752":"SE",
  "826":"GB","756":"CH","578":"NO","352":"IS","688":"RS","070":"BA","008":"AL",
  "807":"MK","499":"ME","498":"MD","804":"UA","112":"BY","643":"RU","792":"TR",
  "268":"GE","051":"AM","031":"AZ","398":"KZ",
  "840":"US","124":"CA","484":"MX","076":"BR","032":"AR","152":"CL","170":"CO",
  "604":"PE","862":"VE","218":"EC","858":"UY","600":"PY","068":"BO","192":"CU",
  "214":"DO","320":"GT","188":"CR","591":"PA","558":"NI","340":"HN","222":"SV",
  "156":"CN","392":"JP","410":"KR","356":"IN","360":"ID","764":"TH","704":"VN",
  "608":"PH","586":"PK","050":"BD","364":"IR","368":"IQ","376":"IL","682":"SA",
  "784":"AE","818":"EG","504":"MA","012":"DZ","788":"TN","434":"LY","231":"ET",
  "404":"KE","834":"TZ","566":"NG","710":"ZA","036":"AU","554":"NZ","800":"UG",
  "180":"CD","562":"NE",
};

const out = [];
for (const f of geo.features) {
  const d = path(f);
  if (!d) continue;
  out.push({
    a2:   N2A[String(f.id).padStart(3, "0")] ?? null,
    name: f.properties.name,
    d:    fmt(d),
  });
}

writeFileSync(
  new URL("../public/scripts/world-paths.js", import.meta.url),
  `// Generated by scripts/gen-world-paths.js from world-atlas countries-50m (Natural Earth).\n` +
  `// Projection: geoNaturalEarth1, ${W}×${Math.round(path.bounds(geo)[1][1])}, integer coords. Do not edit by hand.\n` +
  `window.APERIO_WORLD_PATHS = ${JSON.stringify(out)};\n`
);
console.log(`public/scripts/world-paths.js written: ${out.length} countries`);
